package Hafta2_Odev2.ReCapDemo_Classes;

public class DortIslem {

    public int Topla(int num1, int num2){
        return num1 + num2;
    }

    public int Cikar(int num1, int num2){
        return num1 - num2;
    }

    public int Carp(int num1, int num2){
        return num1 * num2;
    }

    public int Bol(int num1, int num2){
        return num1 / num2;
    }
}
