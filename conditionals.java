public class conditionals {
    public static void main(String[] args) {

        int sayi = 20;
        
        if(sayi==20){
            System.out.println("Sayı 20'ye eşittir");
        }
        if(sayi < 20){
            System.out.println("Sayı 20'den küçüktür");
        }else{
            System.out.println("Sayı 20'den büyüktür");
        }
    }
}
